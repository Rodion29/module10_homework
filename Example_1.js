let message = prompt('Введите ваше число');
let result = ++message;
let remain = result % 2;
if (typeof result === "number", result === result) {
    if (remain == 0) {
        console.log('Получившееся число четное')
    } else {
        console.log('Получившееся число нечетное')
    }
} else {
    console.log('Упс, кажется вы ошиблись')
}